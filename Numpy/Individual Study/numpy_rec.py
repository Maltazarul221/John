#Scalar
#A scalar is a single number. In NumPy, a scalar can be created using a zero-dimensional array.

import numpy as np

A = np.array(8)
print(A)

#Vector
#A vector is a one-dimensional array of numbers or a sequence. It can be represented as:
# Row Vector

A = np.array([3,6,8,1])
print(A)

#Colum Vector

A = np.array([[3] , [6], [8] , [1]])
print(A)